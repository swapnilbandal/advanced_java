package com.app.model;

public class Student {
	private String name;
	private String prn;
	private String m1;
	private String m2;
	private String m3;
	public Student() {
		super();
	}
	public String getName() {
		return name;
	}
	public String getPrn() {
		return prn;
	}
	public  getM1() {
		return m1;
	}
	public String getM2() {
		return m2;
	}
	public String getM3() {
		return m3;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setPrn(String prn) {
		this.prn = prn;
	}
	public void setM1(String m1) {
		this.m1 = m1;
	}
	public void setM2(String m2) {
		this.m2 = m2;
	}
	public void setM3(String m3) {
		this.m3 = m3;
	}
	
	
}
